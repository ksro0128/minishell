#include "../libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	size_t	ind;
	char	*s;

	ind = -1;
	s = malloc(count * size);
	if (!s)
		return (0);
	while (++ ind < count * size)
		s[ind] = 0;
	return ((void *)s);
}
