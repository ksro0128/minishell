int	ft_isalnum(int c)
{
	int	chk;

	chk = 0;
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		chk = 1;
	else if (c >= '0' && c <= '9')
		chk = 1;
	return (chk);
}
