#include "../libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			idx;
	unsigned char	*uc;

	idx = 0;
	uc = (unsigned char *) b;
	while (idx < len)
	{
		uc[idx] = (unsigned char) c;
		idx ++;
	}
	return (b);
}
